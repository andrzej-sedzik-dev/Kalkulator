public class Calculator {

    static public int add(int a, int b) {
        return a + b;
    }
    static public int subtract(int a, int b) {
        return a - b;
    }
    public static void main(String[] args) {
        System.out.println( add(5, 8));
        System.out.println(subtract(5,2));
    }
}

